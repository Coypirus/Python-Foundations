# import math functions
import math

x = math.sqrt(25)
print(x)

x = math.sqrt(15)
print(x)

# Floor = Lower, Ceiling = Higher
print(math.floor(x))
print(math.ceil(x))
print(math.floor(-x))
print(math.ceil(-x))

print(math.pow(3, 2))  # 3 to the second power.

# Constants
print(math.pi)
print(math.e)  # Epsilon

# Assign shorthand name to imported math module
m = math
print(m.pi)

